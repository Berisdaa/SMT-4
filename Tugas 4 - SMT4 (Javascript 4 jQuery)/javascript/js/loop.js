//LOOPING

// WHILE 
console.log("================ WHILE =================");
let mulai = 0;
let selesai = 7;

while (mulai < selesai) {
    console.log(mulai);

    mulai++;
}

console.log("=============== DO WHILE =================");

// DO WHILE 
let start = 10;
let finish = 17;

do {
    console.log(start);
    start++;
} while (start < finish);

console.log("================ FOR 1 ================");

// FOR

let beg = 100;
let end = 111;

for (beg; beg < end; beg++) {
    console.log(beg);
}

console.log("================ FOR 2 ================");

for (let r = 3; r < 7; r++) {
    console.log(r);

}